#!/bin/bash

# Запрос пути к директории от пользователя
read -p "Введите путь к директории для поиска логов: " dirpath

# Проверка существования директории
if [ ! -d "$dirpath" ]; then
  echo "Ошибка: директория '$dirpath' не найдена."
  exit 1
fi

# Поиск всех файлов с расширением .log в директории, сортировка по времени создания
# и вывод 5 самых старых
find "$dirpath" -type f -name "*.log" -printf "%T+ %p\n" | sort | head -n 5
