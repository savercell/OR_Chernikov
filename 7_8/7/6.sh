#!/bin/bash

# Запрос пути к директории от пользователя
read -p "Введите путь к директории: " dirpath

# Проверка, существует ли директория
if [ ! -d "$dirpath" ]; then
  echo "Ошибка: директория не найдена."
  exit 1
fi

# Удаление файлов, измененных более 7 дней назад
find "$dirpath" -type f -mtime +7 -exec rm -f {} \;

echo "Все файлы, измененные более 7 дней назад, удалены из '$dirpath'."
