#!/bin/bash

# Запрос пути к директории от пользователя
read -p "Введите путь к директории: " dirpath

# Проверка, существует ли директория
if [ ! -d "$dirpath" ]; then
  echo "Ошибка: директория не найдена."
  exit 1
fi

# Перебор всех файлов в директории
for file in "$dirpath"/*; do
  # Проверка, является ли объект файлом
  if [ -f "$file" ]; then
    # Получение имени файла и преобразование в строчные буквы
    lowercase_name=$(echo "$(basename "$file")" | tr '[:upper:]' '[:lower:]')

    # Переименование файла
    mv "$file" "$dirpath/$lowercase_name"
  fi
done

echo "Все файлы в директории '$dirpath' переименованы в строчные буквы."
